<p>Este é o tema AdminLTE por <a href="https://almsaeedstudio.com/" target="_blank">https://almsaeedstudio.com/</a>.
    Esta é uma versão reduzida com apenas os estilos e scripts necessários para que o tema funcione. Baixe a versão
