Esta seção é onde você especificar que essa permissão depende do usuário ter uma ou mais outras permissões.<br/><br/>
Por exemplo: Esta permissão pode ser <strong>create-user</strong>, mas se o usuário também não possuir as permissoes
<strong>view-backend</strong> e <strong>view-access-management</strong> ele nunca será capaz de chegar a tela <strong>Create
    User</strong>.